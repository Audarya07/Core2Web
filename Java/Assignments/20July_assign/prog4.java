class Prog4 {

	public static void main(String[] args) {
	    int a=10;
	    long b=7000;
	    float c=41.521f;
	    double d=321.22221;
	    char val='z';
	
	    System.out.println(~a);
	    System.out.println(~b);
	    System.out.println(~c);
	    System.out.println(~d);
	    System.out.println(~val);
	}
}

