class Prog1 {

	public static void main(String[] args) {

		char ch = '-';
		int a = 10, b = 4;
		switch(ch) {

			case '+':
				System.out.println(a+b);
				break;
			case '-':
				System.out.println(a-b);
				break;
			case '*':
				System.out.println(a*b);
				break;
			case '/':
				System.out.println(a/b);
				break;
			case '%':
				System.out.println(a%b);
				break;

		}
	}
}
