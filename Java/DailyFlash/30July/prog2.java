class Prog2 {

	public static void main(String[] args) {

		char ch = 'P';
		switch(ch) {

			case 'J':
				System.out.println("Java");
				break;
			case 'P':
				System.out.println("Python");
				break;
			case 'C':
				System.out.println("C language");
				break;
			case 'K':
				System.out.println("Kotlin");
				break;
			case 'R':
				System.out.println("Rust");
				break;

		}
	}
}
